# =============================================================================
# start.S — 程序入口点 _start
#
# Linux 将控制权交给新进程时：
#   - 栈顶 (rsp) → argc (8 字节)
#   - rsp+8      → argv[0]  (指向程序名的指针)
#   - rsp+16     → argv[1]
#   - ...
#   - NULL       → argv 结束标记
#   - 之后        → envp[0], envp[1], ..., NULL
#
# 在调用 main 之前，必须保证栈 16 字节对齐（System V AMD64 ABI 要求）。
# 进入 _start 时 rsp 已经是 16 字节对齐的（内核保证），
# 但 call 指令会压入 8 字节返回地址，所以被调用函数入口处 rsp ≡ 8 (mod 16)。
# 这里不需要对齐栈帧，因为 main 之前我们只做一次 call。
# =============================================================================

    .global _start
    .text

_start:
    # ---------------------------------------------------------------
    # 1. 准备 main(argc, argv) 的参数
    # ---------------------------------------------------------------
    # argc ← (%rsp)          rdi = 第一个参数
    movq    (%rsp), %rdi

    # argv ← rsp + 8         rsi = 第二个参数
    leaq    8(%rsp), %rsi

    # 2. 即使不需要 envp，System V ABI 也建议在调用前清零
    #    帧指针寄存器 rbp，方便调试器回溯栈帧。
    # ---------------------------------------------------------------
    xorq    %rbp, %rbp

    # ---------------------------------------------------------------
    # 3. 调用 int main(int argc, char *argv[])
    # ---------------------------------------------------------------
    call    main

    # ---------------------------------------------------------------
    # 4. main 返回值在 %rax 中 → 传给 _exit 作为退出码
    # ---------------------------------------------------------------
    movq    %rax, %rdi        # exit status = main() 返回值
    call    toylibc_exit

    # 永远不会执行到这里（toylibc_exit 是 noreturn）
    ud2
